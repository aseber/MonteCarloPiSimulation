public class Sample {

    private double xLocation;
    private double yLocation;

    public Sample(double xLocation, double yLocation) {

        this.xLocation = xLocation;
        this.yLocation = yLocation;

    }

    public double getXLocation() {

        return xLocation;

    }

    public double getYLocation() {

        return yLocation;

    }

}
